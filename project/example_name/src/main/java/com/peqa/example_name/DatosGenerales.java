package com.peqa.example_name;

public class DatosGenerales {
    String nombre;
    String edad;
    String abstracto;

    public void setNombre(String _nombre){
        nombre = _nombre;
    }
    public String getNombre() {
        return nombre;
    }

    public void setEdad(String _edad){
        edad = _edad;
    }
    public String getEdad() {
        return edad;
    }

    public void setAbstracto(String _abstracto){
        abstracto = _abstracto;
    }
    public String getAbstracto(){
        return abstracto;
    }
}
